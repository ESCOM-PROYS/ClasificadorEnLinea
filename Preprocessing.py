#######################################################################################################################
class Preprocessor:
    # -------------------------------------------------------------------------------------------------------------
    def __init__(self):
        pass

    # -------------------------------------------------------------------------------------------------------------
    def process(self, pil_image):
        pass

#######################################################################################################################


class SizePreprocessor(Preprocessor):
    # -------------------------------------------------------------------------------------------------------------
    def __init__(self, width_resize_img_out, height_resize_img_out):
        Preprocessor.__init__(self)
        self.width_resize_img_out = width_resize_img_out
        self.height_resize_img_out = height_resize_img_out

    # -------------------------------------------------------------------------------------------------------------
    def process(self, pil_image):
        return pil_image.resize((self.width_resize_img_out, self.height_resize_img_out))

